package code_b;

public class vidu {
	private int a, b, C;

	public vidu(int a, int b, int c) {
		super();
		this.a = a;
		this.b = b;
		C = c;
	}

	public int getA() {
		return a;
	}

	public void setA(int a) {
		this.a = a;
	}

	public int getB() {
		return b;
	}

	public void setB(int b) {
		this.b = b;
	}

	public int getC() {
		return C;
	}

	public void setC(int c) {
		C = c;
	}
}
